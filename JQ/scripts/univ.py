def to_jq_code(code):
    """6 位代码 -> JQ 代码 (6 开头 .XSHG, 其他 .XSHE)"""
    return code + '.XSHG' if code.startswith('6') else code + '.XSHE'


def get_universe_jq(universe_name):
    """获取指定 universe 的 JQ 代码列表 (含 DISABLE_STOCK 过滤)"""
    raw_map = {
        'HS300': HS300_CODES_RAW,
        'CSI1000': CSI1000_CODES_RAW,
        'CYB_STAR_50': CYB_STAR_50_CODES_RAW,
        'ALL': ALL_CODES_RAW,
    }
    if universe_name not in raw_map:
        raise ValueError('Unknown universe: ' + universe_name)
    filtered = [c for c in raw_map[universe_name] if c not in DISABLE_STOCK]
    return [to_jq_code(c) for c in filtered]

