def compute_signals_jq(stock_list, g, context):
    """入场信号计算 (替代本地 _compute_signal_v2 + secondary)

    主信号: 5 日跌幅 > 10% AND RSI(6) < 20
    副信号: 5 日跌幅 > 8% AND RSI(6) < 30 (仅 BULL)
    """
    n = 30  # 至少 30 日窗口
    df_close = history(n, unit='1d', field='close', security_list=stock_list,
                       df=True, skip_paused=False, fq='pre')
    if df_close is None or df_close.empty:
        return {}

    signals = {}
    for stock in stock_list:
        try:
            close_s = df_close[stock].dropna()
            if len(close_s) < 15:
                continue
        except KeyError:
            continue

        current_close = float(close_s.iloc[-1])
        prev_5_close = float(close_s.iloc[-6]) if len(close_s) >= 6 else current_close
        drop_5d = current_close / prev_5_close - 1

        rsi6 = calc_rsi(close_s, period=6)
        if not np.isfinite(rsi6) or not np.isfinite(drop_5d):
            continue

        # 主信号
        if drop_5d < PARAMS['main_drop_th'] and rsi6 < PARAMS['main_rsi_th']:
            signals[stock] = ('main', rsi6)
        # 副信号 (仅 BULL)
        elif (drop_5d < PARAMS['sec_drop_th']
              and rsi6 < PARAMS['sec_rsi_th']
              and g.regime == 'BULL'):
            signals[stock] = ('secondary', rsi6)

    return signals


def detect_regime_jq(context):
    """状态检测 (简化版, 滞后 3 天避免前视)

    复用本地 detect_full_regime 的核心逻辑:
    - CRASH: 5 日跌幅 > 8% 或 20 日跌幅 > 15%
    - BULL: MA20 > MA60 且 close > MA20 且 MA20 斜率 > 0
    - BEAR: MA20 < MA60
    - SIDEWAYS: 其他
    """
    market = '000300.XSHG'
    h = attribute_history(market, 250, '1d',
                          ['close', 'high', 'low'], df=True, fq='pre')
    if h is None or h.empty:
        return 'SIDEWAYS'

    close = h['close']
    if len(close) < 63:
        return 'SIDEWAYS'
    ma20 = close.rolling(20).mean()
    ma60 = close.rolling(60).mean()

    # T 日用 T-3 数据 (避免 hysteresis 用未来数据)
    c_t3 = float(close.iloc[-4])
    ma20_t3 = float(ma20.iloc[-4]) if not pd.isna(ma20.iloc[-4]) else np.nan
    ma60_t3 = float(ma60.iloc[-4]) if not pd.isna(ma60.iloc[-4]) else np.nan
    if pd.isna(ma20.iloc[-23]) or pd.isna(ma20.iloc[-4]):
        return 'SIDEWAYS'
    slope_ma20 = (ma20.iloc[-4] - ma20.iloc[-23]) / 19.0

    if not all(np.isfinite([c_t3, ma20_t3, ma60_t3, slope_ma20])):
        return 'SIDEWAYS'

    # CRASH 检测
    crash_5d = c_t3 / float(close.iloc[-8]) - 1 if len(close) >= 8 else 0
    crash_20d = c_t3 / float(close.iloc[-23]) - 1 if len(close) >= 23 else 0
    if crash_5d <= -0.08 or crash_20d <= -0.15:
        return 'CRASH'

    # BULL
    if ma20_t3 > ma60_t3 and c_t3 > ma20_t3 and slope_ma20 > 0:
        return 'BULL'

    # BEAR
    if ma20_t3 < ma60_t3:
        return 'BEAR'

    return 'SIDEWAYS'


