def list_all_symbols(data_dir: str = "data/data-by-stock") -> List[str]:
    """列出所有可用的股票代码（6 位）"""
    base = Path(data_dir)
    if not base.exists():
        return []
    syms = []
    for p in base.glob("*_金玥数据.csv"):
        name = p.stem.replace("_金玥数据", "")
        # 剔除北交所（92/83 开头）
        if name.startswith(("92", "83")):
            continue
        syms.append(name)
    return sorted(syms)


def get_hs300(data_dir: str = "data/data-by-stock",
               top_n: int = 300) -> List[str]:
    """沪深 300 启发式选择

    使用流通市值 top_n 作为代理（实际中证指数公司发布的成分股会有差异）
    """
    cache_path = Path("data/processed/v1/stock")
    base = Path(data_dir)
    if not base.exists():
        return []

    # 优先使用缓存中的流通市值排序
    syms_with_mcap = []
    for p in base.glob("*_金玥数据.csv"):
        name = p.stem.replace("_金玥数据", "")
        if name.startswith(("92", "83")):
            continue
        cache_file = cache_path / f"{name}.parquet"
        if cache_file.exists():
            try:
                df = pd.read_parquet(cache_file, columns=["流通市值（元）"])
                if len(df) > 0:
                    latest_mcap = float(df["流通市值（元）"].dropna().iloc[-1])
                    syms_with_mcap.append((name, latest_mcap))
                    continue
            except Exception:
                pass
        # 无缓存时退回原文件读最近一行
        try:
            df = pd.read_csv(p, usecols=["代码", "流通市值（元）"],
                              dtype={"代码": str})
            if len(df) > 0:
                mcap = float(df["流通市值（元）"].dropna().iloc[-1])
                syms_with_mcap.append((name, mcap))
        except Exception:
            pass

    syms_with_mcap.sort(key=lambda x: -x[1])
    return [s for s, _ in syms_with_mcap[:top_n]]


def get_csi1000(data_dir: str = "data/data-by-stock",
               use_official: bool = True,
               liquidity_filter: bool = False,
               min_avg_amount: float = 100_000_000) -> List[str]:
    """中证 1000 股票池

    Args:
        data_dir: 个股数据目录（备用，主要给启发式 fallback）
        use_official: True=用官方成分股（CSI1000_OFFICIAL，1000 只精确），
                     False=用流通市值 top 1000 启发式
        liquidity_filter: True=按日均成交额过滤（ATOS2 v1 新增）
        min_avg_amount: 最小日均成交额（元），默认 100 万

    Returns:
        股票代码列表（按官方顺序或按市值降序）
    """
    if use_official and not liquidity_filter:
        # 优先返回官方成分股（顺序固定）
        return list(CSI1000_OFFICIAL)

    if use_official and liquidity_filter:
        # ATOS2 v1：按流动性过滤官方成分股
        cache_path = Path("data/processed/v1/stock")
        base = Path(data_dir)
        if not base.exists():
            return list(CSI1000_OFFICIAL)

        kept = []
        for sym in CSI1000_OFFICIAL:
            cache_file = cache_path / f"{sym}.parquet"
            if not cache_file.exists():
                continue
            try:
                df = pd.read_parquet(cache_file, columns=["amount"])
                if len(df) >= 20:
                    avg_amt = float(df["amount"].tail(20).dropna().mean())
                    if avg_amt >= min_avg_amount:
                        kept.append(sym)
            except Exception:
                pass
        return kept

    # 启发式 fallback（仅在用户明确要求时使用）
    cache_path = Path("data/processed/v1/stock")
    base = Path(data_dir)
    if not base.exists():
        return []

    syms_with_mcap = []
    for p in base.glob("*_金玥数据.csv"):
        name = p.stem.replace("_金玥数据", "")
        if name.startswith(("92", "83")):
            continue
        cache_file = cache_path / f"{name}.parquet"
        if cache_file.exists():
            try:
                df = pd.read_parquet(cache_file, columns=["流通市值（元）"])
                if len(df) > 0:
                    latest_mcap = float(df["流通市值（元）"].dropna().iloc[-1])
                    if latest_mcap > 0:
                        syms_with_mcap.append((name, latest_mcap))
                    continue
            except Exception:
                pass
        try:
            df = pd.read_csv(p, usecols=["代码", "流通市值（元）", "是否ST"],
                              dtype={"代码": str})
            if len(df) > 0:
                is_st = str(df["是否ST"].iloc[-1]) == "是"
                delist_date = df["退市时间"].iloc[-1] if "退市时间" in df.columns else None
                if is_st or pd.notna(delist_date) and delist_date != "-":
                    continue
                mcap = float(df["流通市值（元）"].dropna().iloc[-1])
                if mcap > 0:
                    syms_with_mcap.append((name, mcap))
        except Exception:
            pass

    syms_with_mcap.sort(key=lambda x: -x[1])
    return [s for s, _ in syms_with_mcap[:1000]]


